public enum IngredientUnitEnum
{
    Gram,          // g - for weight
    Kilogram,      // kg - for weight
    Milligram,     // mg - for weight
    Pound,         // lb - for weight

    Liter,         // L - for volume
    Milliliter,    // mL - for volume
    FluidOunce,    // fl oz - for volume
    Cup,           // cup - for volume
    Pint,          // pt - for volume
    Quart,         // qt - for volume
    Gallon,        // gal - for volume

    Teaspoon,      // tsp - for small quantities
    Tablespoon,    // tbsp - for small quantities

    Piece,         // pc - for countable items (e.g., 1 egg, 2 apples)
    Slice,         // slice - for items cut into pieces (e.g., bread, cheese)
    Pinch,         // pinch - for very small amounts, often used for spices
    Dash,          // dash - similar to a pinch, often used for liquids or spices

    Bunch,         // bunch - for grouped items (e.g., a bunch of herbs)
    Clove,         // clove - specific to garlic
    Stick,         // stick - for items like butter
}